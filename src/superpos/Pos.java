    private void txtProductCodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtProductCodeKeyPressed
         // TODO add your handling code here:
         if(evt.getKeyCode()== KeyEvent.VK_ENTER){
             
             String name = txtProductCode.getText();
             
             try {
                 Class.forName("com.mysql.jdbc.Driver");
                 con = DriverManager.getConnection("jdbc:mysql://localhost/javadb","root","elijah");
                 pst = con.prepareStatement("select * from product where barcode =?");
                 pst.setString(1, name);
                 rs = pst.executeQuery();
                 
                 if(rs.next()== false){
                     JOptionPane.showMessageDialog(this, "Barcode Not Found");
                 }else{
                     String productName = rs.getString("product");
                     String price = rs.getString("cost_price");
                     
                     txtProductName.setText(productName);
                     txtPrice.setText(price);
                 }
             } catch (ClassNotFoundException ex) {
                 Logger.getLogger(Pos.class.getName()).log(Level.SEVERE, null, ex);
             } catch (SQLException ex) {
                 Logger.getLogger(Pos.class.getName()).log(Level.SEVERE, null, ex);
             }
            
         }
    }//GEN-LAST:event_txtProductCodeKeyPressed

