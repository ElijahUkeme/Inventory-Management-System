    private void pos(){
        String name = txtProductCode.getText();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/javadb","root","elijah");
                 pst = con.prepareStatement("select * from product where barcode =?");
                 pst.setString(1, name);
                 rs = pst.executeQuery();
                 
                 while(rs.next()){
                     int currentQuantity = rs.getInt("quantity");
                     if(txtQuantity.getText().toString().trim().isEmpty()){
                         JOptionPane.showMessageDialog(this, "Please enter the quantity");
                     }
                     int price = Integer.parseInt(txtPrice.getText());
                     int requiredQuantity = Integer.parseInt(txtQuantity.getText());
                     int total = price * requiredQuantity;
                     
                     
                     
                     if(requiredQuantity > currentQuantity){
                         JOptionPane.showMessageDialog(this, "Quantity is not enough\nAvailable Quantity is "+currentQuantity);
                     }else{
                          model = (DefaultTableModel)pos_table.getModel();
                         
                         model.addRow(new Object[]{
                             
                         txtProductCode.getText(),
                         txtProductName.getText(),
                         txtPrice.getText(),
                         txtQuantity.getText(),
                         total,
                         });
                     }
                     
                     int sum = 0;
                     
                     for(int i =0;i<pos_table.getRowCount();i++){
                         sum += Integer.parseInt(pos_table.getValueAt(i, 4).toString());
                     }
                     
                     txtSubTotal.setText(Integer.toString(sum));
                     
                     txtProductCode.setText("");
                     txtProductName.setText("");
                     txtPrice.setText("");
                     txtQuantity.setText("");
                     txtProductCode.requestFocus();
                     
                     
                 }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Pos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Pos.class.getName()).log(Level.SEVERE, null, ex);
        }
                 
        
    }
    
